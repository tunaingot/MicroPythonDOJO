# スイッチが離されたイベント
def switch_released():
    print("スイッチが離されました！")
    #---------------------------------------------------------------------------
    # スイッチが離された時の処理をここに書いてください
    #---------------------------------------------------------------------------

# スイッチ長押しを検出したイベント
def switch_long_pushed():
    print("スイッチが長押しされました！")
    #---------------------------------------------------------------------------
    # スイッチが長押しされた時の処理をここに書いてください
    #---------------------------------------------------------------------------

def switch_long_released():
    print("スイッチ長押しが解除されました！")
