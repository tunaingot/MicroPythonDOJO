from machine import Pin
import time
from systimer import systimer

#===============================================================================
# ハードウェアの設定
#===============================================================================
led = Pin(25, Pin.OUT)
button = Pin(14, Pin.IN, Pin.PULL_UP)
systimer = systimer()

#===============================================================================
# 定数
#===============================================================================
INTERVAL_SLOW_DEFAULT = 40   # x 10msec, 0x28, XORで値を変えられる絶妙な値設定(^ ^)
INTERVAL_FAST_DEFAULT = 8    # x 10msec, 0x08
LONG_PRESS_DETECT_TIME = 100 # x 10msec スイッチが押されてから長押しとみなすまでの時間

#===============================================================================
# 書き換えてほしくない変数
#===============================================================================
_switch_scan_shiftReg = 0
_flash_count = 0
_switch_push_count = 0

#===============================================================================
# 自由に書き換えてもいい変数
#===============================================================================
flash_interval = INTERVAL_SLOW_DEFAULT    #点滅の間隔（systimerの割り込み回数）

#===============================================================================
# イベント
#===============================================================================
# スイッチが押されたイベント
def switch_pushed():
    global flash_interval
    
    print("スイッチが押されました！")
    #---------------------------------------------------------------------------
    # スイッチが押された時の処理をここに書いてください
    #---------------------------------------------------------------------------
    flash_interval ^= 0x20
