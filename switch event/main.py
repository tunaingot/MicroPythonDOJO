    #---------------------------------------------------------------------------

#===============================================================================
# メインループ
#===============================================================================
while True:
    if systimer.interruptCt() != 0: #10msecごとに割り込みが発生しているなら
        #-----------------------------------------------------------------------
        # 書き換えないで！スイッチ入力監視
        #-----------------------------------------------------------------------
        _switch_scan_shiftReg <<= 1
        _switch_scan_shiftReg &= 0x00ff

        if button.value() == 0:
            _switch_scan_shiftReg |= 0x01

        #-----------------------------------------------------------------------
        # 書き換えないで！イベント生成
        #-----------------------------------------------------------------------
        if _switch_scan_shiftReg == 0x7f:   #スイッチが押された
            _switch_push_count += 1 #長押しの監視開始
            switch_pushed()                 #スイッチが押されたイベント発生

        if _switch_scan_shiftReg == 0x80:   #スイッチが離された
            if _switch_push_count >= LONG_PRESS_DETECT_TIME:    #長押しされていたなら
                switch_long_released()
            _switch_push_count = 0  #長押しの監視終了
            switch_released()               #スイッチが離されたイベント発生
            
        if _switch_push_count > 0:
            _switch_push_count += 1
            if _switch_push_count == LONG_PRESS_DETECT_TIME: #スイッチが押されてから長押しとみなす時間経過しているなら
                switch_long_pushed()

        #-----------------------------------------------------------------------
        # 自由に書き換えてもいい処理
        #-----------------------------------------------------------------------
        _flash_count += 1
        _flash_count %= flash_interval
        
        if _flash_count == 0:
            led.toggle()#点灯/消灯を切り替える
